'''
题目：打印出如下图案（菱形）:
   *
  ***
 *****
*******
 *****
  ***
   *
先把图形分成两部分来看待，前四行一个规律，后三行一个规律，利用双重for循环，
第一层控制行，第二层控制列
'''
for i in range(4):
    print(' '*(3-i),'*'*(2*i+1),' '*(3-i))
for i in list(range(3))[::-1]:
    print(' '*(3-i),'*'*(2*i+1),' '*(3-i))